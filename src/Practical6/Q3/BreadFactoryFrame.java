        private void getNumOfLoaves() {
                this.breadFactory.getBread(0).setNoOfLoaves(Integer.parseInt(plain_input.getText()));
                this.breadFactory.getBread(1).setNoOfLoaves(Integer.parseInt(wholemeal_input.getText()));
                this.breadFactory.getBread(2).setNoOfLoaves(Integer.parseInt(raisin_input.getText()));
                this.breadFactory.getBread(3).setNoOfLoaves(Integer.parseInt(banana_input.getText()));
                this.breadFactory.getBread(4).setNoOfLoaves(Integer.parseInt(corn_input.getText()));
        }

        public static String spaces(int num) {
                String result = "";
                for (int i = 0; i < num; i++) {
                        result += " ";
                }
                return result;
        }

        private void displayCosts() {
                String msg = "Type\tNumber of Loaves\tTotal Cost\n";
                msg += "====\t================\t=============\n";

                for (int i = 0; i < breadFactory.getNumOfBreads(); i++) {
                        Bread b = breadFactory.getBread(i);
                        double costs = b.getNoOfLoaves() * b.getAmtOfFlour() * this.breadFactory.getCostOfFlour();
                        msg += b.getName() + "\t" + b.getNoOfLoaves() + "\t\t$" + costs + "\n";
                }
                output_text_field.setText(msg);
        }

