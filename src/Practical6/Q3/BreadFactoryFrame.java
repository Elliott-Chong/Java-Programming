        private void save_to_file_btnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_save_to_file_btnActionPerformed
                /*
                 * 
                 * save to file in this format:
                 * 0.05
                 * Plain, 100
                 * Wholemeal, 200
                 * Raisin,300
                 * Banana, 400
                 * Corn, 500
                 */

                try {
                        FileWriter fw = new FileWriter("src/Practical6/Q3/bread.txt");
                        BufferedWriter bw = new BufferedWriter(fw);
                        PrintWriter pw = new PrintWriter(bw);

                        pw.println(flour_cost_input.getText());
                        pw.println("Plain, " + plain_input.getText());
                        pw.println("Wholemeal, " + wholemeal_input.getText());
                        pw.println("Raisin, " + raisin_input.getText());
                        pw.println("Banana, " + banana_input.getText());
                        pw.println("Corn, " + corn_input.getText());

                        pw.close();
                        bw.close();
                        fw.close();

                        // serialise the breadFactory object to a file called breadFactory.dat
                        FileOutputStream fileOut = new FileOutputStream("src/Practical6/Q3/breadFactory.dat");
                        ObjectOutputStream out = new ObjectOutputStream(fileOut);
                        out.writeObject(this.breadFactory);
                        out.close();
                        fileOut.close();

                } catch (IOException e) {
                        e.printStackTrace();
                }
        }// GEN-LAST:event_save_to_file_btnActionPerformed

