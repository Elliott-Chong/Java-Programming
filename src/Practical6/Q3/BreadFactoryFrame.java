        private void retrieve_buttonActionPerformed(java.awt.event.ActionEvent evt) {
                // TODO add your handling code here:
                // load the breadFactory.dat serialized file
                try {
                        FileInputStream fileIn = new FileInputStream("src/Practical6/breadFactory.dat");
                        ObjectInputStream in = new ObjectInputStream(fileIn);
                        this.breadFactory = (BreadCollection) in.readObject();
                        in.close();
                        fileIn.close();
                } catch (IOException i) {
                        i.printStackTrace();
                        return;
                } catch (ClassNotFoundException c) {
                        System.out.println("BreadFactory class not found");
                        c.printStackTrace();
                        return;
                }
        }

